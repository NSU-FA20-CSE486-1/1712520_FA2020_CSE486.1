package com.afridi.BanglaDictionary;


public class Words {
    private String english;
    private String bangla;

    public Words(String english, String bangla) {
        this.english = english;
        this.bangla = bangla;
    }
    public String getEnglish() {
        return english;
    }

    public String getBangla() {
        return bangla;
    }
}
